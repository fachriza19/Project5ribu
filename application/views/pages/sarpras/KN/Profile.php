                            <h6>Tugas Kapal Negara: </h6>
                            <h6><ol>
                              <li><h6>Penempatan, perawatan, perbaikan dan pengoperasian sarana bantu navigasi pelayaran.</h6></li>
                              <li><h6>Gilir tugas dan penyaluran logistik petugas menara suar.</h6></li>
                              <li><h6>Pengangkutan penumpang dinas, pendistribusian BBM / Gas dan peralatan sarana bantu navigasi pelayaran.</h6></li>
                              <li><h6>Pengamatan fungsi kerja sarana bantu navigasi pelayaran.</h6></li>
                              <li><h6>Survey kenavigasian.</h6></li>
                              <li><h6>Bantuan pengumpulan data meteorologi untuk kepentingan analisis ramalan cuaca.</h6></li>
                              <li><h6>Bantuan SAR (Search And Recue).</h6></li>
                            </ol></h6>
                            <br>

                            <h6>PERBANTUAN KAPAL NEGARA KENAVIGASIAN: </h6>
                            <h6><ol>
                              <li><h6>Dilaksanakan mengingat kemampuan yang terbatas dari kapal-kapal Distrik Navigasi Klas II dan Distrik Navigasi Klas III.</h6></li>
                              <li><h6>Kepala Distrik Navigasi Klas II dan Distrik Navigasi Klas III mengajukan usulan permintaan bantuan kapal berkala setiap awal tahun kepada Kepala Distrik Navigasi Klas I dan Distrik Navigasi Klas II yang bertanggung jawab memberikan perbantuan dengan tembusan kepada Direktur Kenavigasian.</h6></li>
                              <li><h6>Berdasarkan permintaan bantuan tersebut, Kepala Distrik Navigasi Klas I dan Distrik Navigasi Klas II membuat rencana operasi kapal secara terpadu yang disesuaikan dengan kemampuan / kondisi kapal dan anggaran, selanjutnya disampaikan kepada Direktur Kenavigasian dengan tembusan Distrik Navigasi Klas II dan Distrik Navigasi Klas III.</h6></li>
                              <li><h6>Perbantuan kapal dapat diberikan sesuai dengan kemampuan operasional kapal, apabila Distrik Navigasi Klas I dan Klas II tidak mampu memberikan perbantuan operasi, maka permintaan perbantuan operasi diteruskan kepada Direktur Kenavigasian.</h6></li>
                              <li><h6>Sesudah menerima permintaan perbantuan operasi, Direktur Kenavigasian memerintahkan kepada Kepala Distrik Navigasi Klas I dan Klas II terdekat lainnya atau Kepala Balai Teknologi Keselamatan Pelayaran untuk memberikan perbantuan tersebut.</h6></li>
                            </ol></h6>
