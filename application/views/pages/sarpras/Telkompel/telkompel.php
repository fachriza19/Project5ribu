                             <ol>
                               <li><h6>Di dalam Global Maritime Distress and Safety System (GMDSS) sebagaimana dimaksud dalam Peraturan Menteri Perhubungan Nomor :  PM 26 Tahun 2011 tentang Telekomunikasi Pelayaran berfungsi untuk :</h6></li>
                               <ol type="a">
                                 <li><h6>Pemberitahuan tentang adanya musibah marabahaya (alerting).</h6></li>
                                 <li><h6>Komunikasi untuk koordinasi SAR.</h6></li>
                                 <li><h6>Komunikasi di lokasi musibah.</h6></li>
                                 <li><h6>Tanda untuk memudahkan penentuan lokasi.</h6></li>
                                 <li><h6>Pemberitahuan informasi mengenai keselamatan pelayaran</h6></li>
                                 <li><h6>Komunikasi radio umum, dan</h6></li>
                                 <li><h6>Komunikasi antar anjungan kapal.</h6></li>
                               </ol><br>
                               <li><h6>Di dalam Vessel Traffic  Service (VTS) sebagaimana dimaksud dalam Peraturan Menteri Perhubungan Nomor :  PM 26 Tahun 2011 tentang Telekomunikasi Pelayaran berfungsi untuk :</h6></li>
                               <ol type="a">
                                 <li><h6>Memonitor lalu lintas pelayaran dan alur lalu lintas pelayaran.</h6></li>
                                 <li><h6>Meningkatkan keamanan lalu lintas pelayaran.</h6></li>
                                 <li><h6>Meningkatkan efisiensi bernavigasi.</h6></li>
                                 <li><h6>Perlindungan lingkungan.</h6></li>
                                 <li><h6>Pengamatan, Pendeteksian dan penjejakan kapal di wilayah cakupan VTS.</h6></li>
                                 <li><h6>Pengaturan informasi umum.</h6></li>
                                 <li><h6>Pengaturan informasi khusus, dan</h6></li>
                                 <li><h6>Membantu kapal-kapal yang memerlukan bantuan khusus.</h6></li>
                               </ol>
                               <li><h6>Di dalam Ship Reporting System (SRS) sebagaimana dimaksud dalam Peraturan Menteri Perhubungan Nomor :  PM 26 Tahun 2011 tentang Telekomunikasi Pelayaran berfungsi untuk :</h6></li>
                               <ol type="a">
                                 <li><h6>Menyediakan informasi yang up to date atas gerakan kapal.</h6></li>
                                 <li><h6>Mengurangi interval waktu kontak dengan kapal.</h6></li>
                                 <li><h6>Menentukan lokasi dengan cepat, saat kapal dalam bahaya yang tidak diketahui posisinya, dan</h6></li>
                                 <li><h6>Meningkatkan keamanan dan keselamatan jiwa dan harta benda di laut.</h6></li>
