            <h6>Bengkel merupakan instalasi yang berfungsi untuk melaksanakan pemeliharaan, perawatan dan perbaikan Sarana Bantu Navigasi Pelayaran, Telekomunikasi Pelayaran, Kapal Negara Kenavigasian, Pengamatan Laut dan Fasilitas Pangkalan Kenavigasian.</h6><br>
            <h6>Guna menyelenggarakan tugas pemeliharaan, perawatan dan perbaikan Sarana dan Prasarana Kenavigasian, Bengkel terbagi atas beberapa unit kerja, yaitu :</h6><br>

            <ol><h6>
                <li>Unit Mesin.</li>
                Melaksanakan pemeliharaan, perbaikan, perakitan dan pembuatan komponen dengan cara perautan dan atau menggunakan mesin bubut, mesin frais, mesin skrap dan bor yang berasal dari instalasi lain atau dari unit lain yang ada di bengkel
                <li>Unit Las dan Pipa.</li>
                Melaksanakan pemeliharaan, perbaikan, perakitan dan pembuatan komponen SBNP yang menggunakan gas, peralatan optik SBNP dan alat-alat navigasi non elektrik.
                <li>Unit Gas dan Optik.</li>
                Melaksanakan pemeliharaan, perbaikan, perakitan dan pembuatan komponen tanpa perautan atau menggunakan mesin las, mesin roll plat, mesin pembengkok pipa, mesin press, mesin potong plat dan tungku tempa yang berasal dari instalasi lain atau dari unit lain instalasi bengkel.
                <li>Unit Elektro dan Elektronika.</li>
                Melaksanakan pemeliharaan, perbaikan, perakitan dan pembuatan komponen motor listrik, generator, AC, instalasi listrik pada sarana & prasarana kenavigasian, peralatan elektronik pada stasiun radio pemancar dan penerima dan alat-alat navigasi elektronika di kapal kenavigasian.
                <li>Unit Mekanis.</li>
                Melaksanakan pengoperasian alat angkut, alat angkat dan melaksanakan pemeliharaan, perawatan serta perbaikan dari mesin diesel/bensin, pompa, kompressor, alat angkut/angkat, yang ada di bengkel dan instalasi lainnya.
                <li>Unit Konstruksi.</li>
                Melaksanakan pemeliharaan, perbaikan, perakitan dan pembuatan komponen untuk konstruksi / sipil / bangunan, kayu dan baja yang berasal dari instalasi lain atau unit pada bengkel dan fasilitas pangkalan kenavigasian.
                <li>Unit Galangan.</li>
                Melaksanakan perlimbungan kapal secara rutin dan  merawat serta memperbaiki peralatan galangan.
                <li>Evaluasi Pelaporan dan Gambar.</li>
                Perencanaan kerja, gambar, menginventarisir peralatan bengkel, mengajukan permintaan, menerima, menyimpan dan mengeluarkan bahan kerja dan peralatan kerja bengkel.
              </h6></ol><br>

